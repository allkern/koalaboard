int xy = 0;

uint16_t get_char_uv(int c) {
#ifdef USE_FONT8
    return ((c & 0xf0) << 7) | ((c & 0xf) << 3);
#else
    return ((c & 0xf0) << 8) | ((c & 0xf) << 3);
#endif
}

chip8_t c8;
uint8_t screen[64 * 32];
uint8_t mem[0x1000];

int get_c8_key(int k) {
    if (k == '1') return 0x1;
    if (k == '2') return 0x2;
    if (k == '3') return 0x3;
    if (k == '4') return 0xc;
    if (k == 'q') return 0x4;
    if (k == 'w') return 0x5;
    if (k == 'e') return 0x6;
    if (k == 'r') return 0xd;
    if (k == 'a') return 0x7;
    if (k == 's') return 0x8;
    if (k == 'd') return 0x9;
    if (k == 'f') return 0xe;
    if (k == 'z') return 0xa;
    if (k == 'x') return 0x0;
    if (k == 'c') return 0xb;
    if (k == 'v') return 0xf;
    return -1;
}

void chip8() {
    xy = 0;

    mmio_write_32(GPU_GP0, 0x02080808);
    mmio_write_32(GPU_GP0, 0x00000000);
    mmio_write_32(GPU_GP0, 0x01e00280);

    c8_init(&c8, screen, mem);
    c8_load_program(&c8, rawData, sizeof(rawData));

    int last_k = -1;

    while (1) {
        // Wait for GPU IRQ
        while ((mmio_read_32(0x9f801070) & 1) == 0);

        // Acknowledge GPU IRQ
        mmio_write_32(0x9f801070, ~1ul);

        if (mmio_read_8(UART_LSR) & LSR_RX_READY) {
            int k = get_c8_key(mmio_read_8(UART_RHR));

            if (k != -1) {
                c8_keydown(&c8, k);

                last_k = k;
            }
        } else {
            if (last_k != -1)
                c8_keyup(&c8, last_k);

            last_k = -1;
        }

        int ips = 60;

        while (ips--)
            c8_execute_instruction(&c8);

        mmio_write_32(GPU_GP0, 0x02080808);
        mmio_write_32(GPU_GP0, 0x00000000);
        mmio_write_32(GPU_GP0, 0x01e00280);

        for (int y = 0; y < 32; y++) {
            for (int x = 0; x < 64; x++) {
                mmio_write_32(GPU_GP0, 0x70000000 | (c8.screen[x + (y * 64)] ? 0x0000AAFF : 0x000044AA));
                mmio_write_32(GPU_GP0, ((y * 8) << 16) | (x * 8));
            }
        }
    }
}

void shell() {
    char buf[64];
    char* ptr = buf;

    for (int i = 0; i < 64; i++)
        buf[i] = 0;
    
    kprintf("\r> %s", buf);

    int c = tty_getchar();

    while (1) {
        kprintf("\r> %s", buf);

        while (c == -1)
            c = tty_getchar();

        if (c == '\r') {
            *ptr = 0;

            tty_putchar('\n');

            if (!strcmp(buf, "help")) {
                kprintf("Available commands:\n");
                kprintf("clear      - Clears the screen\n");
                kprintf("help       - Show a list of available commands\n");
                kprintf("ver        - Display KoalaOS' version information\n");
            } else if (!strcmp(buf, "ver")) {
                kprintf("KoalaOS 0.1-%s (%s %s %s %s)\n",
                    STR(COMMIT_HASH),
                    __COMPILER__,
                    __ARCH__,
                    __VERSION__,
                    STR(OS_INFO)
                );
            } else if (!strcmp(buf, "clear")) {
                xy = 0;

                mmio_write_32(GPU_GP0, 0x02080808);
                mmio_write_32(GPU_GP0, 0x00000000);
                mmio_write_32(GPU_GP0, 0x01e00280);
            } else if (!strcmp(buf, "chip8")) {
                chip8();
            } else {
                kprintf("Unrecognized command \'%s\'\n", buf);
            }

            tty_putchar('\n');

            for (int i = 0; i < 64; i++)
                buf[i] = 0;

            ptr = buf;

            goto next;
        } else if (c == '\b') {
            *--ptr = 0;

            goto next;
        }

        *ptr++ = c;
        
        kprintf("\r> %s", buf);

        next:

        kprintf("\r> %s", buf);

        c = tty_getchar();
    }
}

int main() {
    kprintf("Welcome to KoalaOS!\n\n");

    shell();

    return 0;
}

void gpu_putchar(int c) {
    if (c == '\n') {
        xy &= 0xffff0000;

#ifdef USE_FONT8
        xy += 0x80000;
#else
        xy += 0x100000;
#endif
        return;
    } else if (c == '\r') {
        xy &= 0xffff0000;

        return;
    }

    uint16_t uv = get_char_uv(c);

    mmio_write_32(GPU_GP0, 0x65000000);
    mmio_write_32(GPU_GP0, xy);
    mmio_write_32(GPU_GP0, 0x78000000 | uv);

#ifdef USE_FONT8
    mmio_write_32(GPU_GP0, 0x00080008);
#else
    mmio_write_32(GPU_GP0, 0x00100008);
#endif

    xy += 8;
}

